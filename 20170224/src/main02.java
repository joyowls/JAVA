
public class main02 {

	public static void main(String[] args) {
		//1) UpCasting
		//	자식 is a 부모 라는 문장이 어색하지 않다.(원은 도형이다..(o))
		Shape shape=new Circle(10);
		shape.showShapeInfo();
		
		//2) DownCasting
		// 부모 is a 자식 이라는 문장이 어색하다.(도형은 원이다..(x))
		
		//Circle circle = new Shape();	//error! 도형은 원이다 라는 관계가 설정 될 순 없다.
		
		//UpCasting이 먼저 수행 되어야 한다.
		Square square = new Square(3,6);
		Shape squareShape = square; // Shape squareShape = new Square(3,6);
		
		Shape squareShape2 = new Square(3,6);
		
		Square downSquare = (Square)squareShape; //이 도형은 사각형 이다(o)
		Square downSquare2 = (Square)squareShape2;
				
		//Circle errorCircle = (Circle)squareShape;	//컴파일 에러가 없다.
										//실행시 에러가 발생한다.
		
		//연산자 instanceof
		// A instanceof B => boolean 결과값
		// A는 B클래스로 부를 수 있는가? or A는 B클래스로 이루어져 있는가?
		// ex) 부모 instanceof 자식 => 이 부모를 자식이라고 부를 수 있는가?
							     //(이 도형을 사각형이라고 할 수 있는가?)
		// ex) 자식 instanceof 부모 => 이 자식을 부모를 기반으로 하는가? 
								 //(이 사각형은 도형을 기반으로 만들어져 있는가?)
		
		//이 도형이 사각형입니까?
		if(squareShape instanceof Square){
			System.out.println("사각형 입니다");
		}
		if(shape instanceof Circle){
			System.out.println("원 입니다");
		}else{
			System.out.println("원도, 사각형도 아닙니다.");
		}
		
		//사각형이 도형입니까?
		if(square instanceof Shape){
			System.out.println("사각형은 도형입니다");
		}
		
	}

}
